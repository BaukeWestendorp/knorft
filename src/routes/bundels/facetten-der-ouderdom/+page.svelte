<section>
	<h3>Veganisme</h3>
	<p>
		Als 11-jarige knaap leerde mijn vader mij al om konijnen en kippen te slachten en de kadavers te
		reinigen. Ook mijn oom was niet vies van een strooppartijtje en wist geregeld een haasje te
		verschalken. Zijn zoon en alle neven van onze generatie - mijzelf inbegrepen - mochten al eens
		zo'n beest villen en uitbenen.
	</p>
	<p>
		Thuis een varken slachten was nog toegelaten in mijn jeugdjaren. De slachter kwam aan huis,
		zette het mes in de keel en ik ving het bloed op in een teil, zodat we er bloedworst van konden
		draaien. Heel het gezin - ook mijn jongere broer en mijn drie zussen - werd ingeschakeld, want
		het slachten van een varken was een hele dag werk (al dat vlees moest snel in de diepvries).
		Onze varkens slachtten we wanneer ze tussen de 90 en 100 kg wogen; het vlees was dus niet al te
		vet.
	</p>
	<p>
		Duiven haalden we bij onze buurman die zo'n beest zonder veel poeha de nek omwrong, of mij dat
		liet doen.
	</p>
	<p>
		Als knaap wist ik al goed hoe je een dier snel en vrij pijnloos om het hoekje kon helpen.
		Konijnen met één slag van een stevige stok in de nek, een kip de kop afsnijden met één enkele
		haal van een vlijmscherp geslepen mesje, en een duif door de kop tussen wijsvinger en duim in
		één seconde 360° om te draaien.
	</p>
	<p>
		Daarmee was ik geen uitzondering in het dorp: nagenoeg elke leeftijdsgenoot leerde dat van zijn
		ouders - de meisjes zowel als de jongens. Het maakte destijds deel uit van het dagelijkse
		bestaan en we ervoeren het als even gewoon als het rooien van de aardappelen, het uit de grond
		trekken van een rijpe ui, of het afsnijden van een mooie kropsla.
	</p>
	<p>
		Niet iedereen bleek daar echter toe in staat: mijn tweede zus zou het nooit over haar hart
		hebben kunnen verkrijgen. Toch is ze nooit vegetariër geworden. En hoewel ze het doden van
		dieren ronduit verafschuwde, heeft ze het haar (één jaar jongere) broer nooit verweten dat hij
		zonder ook maar één keer met de ogen te knipperen zo'n lief konijntje met een welgemikte
		stokslag in één seconde tijd doodde.
	</p>
	<p>Ik had echter wel voldoende tact om dat niét te doen waar zij bij was.</p>
</section>

